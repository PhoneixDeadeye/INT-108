# Comparison

print(a == b) #Equal

print(a != b) #Not equal

print (a>b) #greater than

print (a<b)  #less than

print (a>=b) #greater than equal

print (a<=b)  #less than equal


# Logical Operators

print(a < 5 and b < 8) 

print(a < 5 and b > 8)

print(a < 5 or b > 8) 

print(a > 3 or b> 10)


print(not(a < 5 and b < 8)) 