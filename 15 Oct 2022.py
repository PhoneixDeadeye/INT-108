# OPERATORS

a=10

b=5

print(a+b) #addition

print(a-b) #substraction

print(a*b) #multiplication

print(a/b) #division

print(a%b) #Modulus

print(a // b) 

print(a ** b) #//exponential


# Assignment Operators

